        name: String,
        description: String,
        ingredients: List<String>,
        steps: List<String>,
        calories: Int,
        protein: Int,
        carbs: Int,
        fat: Int,
        category: MealCategory,
        diets: List<DietType>,
    ): Meal = Meal(
        name = name,
        description = description,
        ingredients = ingredients.joinToString("\n"),
        steps = steps.joinToString("\n"),
        calories = calories,
        proteinG = protein,
        carbsG = carbs,
        fatG = fat,
        category = category,
        dietTypes = diets.joinToString(",") { it.name },
        isAiGenerated = false,
        createdAt = 0L,
    )
